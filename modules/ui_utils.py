import os

def clear_screen():
    """Очищає екран для кращої візуалізації."""
    os.system('cls' if os.name == 'nt' else 'clear')

def print_banner(title):
    """Друкує банер програми з динамічним заголовком."""
    print("╔════════════════════════════════════════╗")
    print(f"║     🖥️  {title} 🖥️       ║")
    print("╚════════════════════════════════════════╝\n")

def pause(message="🔄 Натисніть Enter для продовження..."):
    """Зупиняє виконання програми до введення користувача."""
    input(message)
